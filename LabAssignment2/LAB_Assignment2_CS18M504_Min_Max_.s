.data
data_items: .word 10,4,28,100,100,1000,2,8,0

//INITIALIZATION
	//LOAD INPUT
	LDR R1,=data_items
	
	//R2 - MINIMUM
	LDR R2,[R1]
	
	//R3 - MAXIMUM
	LDR R3,[R1]
	
	//COUNTER TO INCREMENT	
	MOV R6,#1
	
	//REGISTER TO HOLD VALUE TO TERMINATE
	MOV R7,#0

//ITERATION TO COMPARE ELEMENTS AND UPDATE MIN AND MAX VALUES
ITERATE: LDR R4,[R1],#4

	CMP R4,R7
		BEQ TERMINATE
	
	ADD R5,R5,R6 //INCREMENT
	
	CMP R4,R2
		BLT UPDATE_MIN
	
	CMP R4,R3
		BGT UPDATE_MAX
	
	B ITERATE

	UPDATE_MIN: MOV R2,R4	
		B ITERATE

	UPDATE_MAX: MOV R3,R4
		B ITERATE

//EXIT
TERMINATE: